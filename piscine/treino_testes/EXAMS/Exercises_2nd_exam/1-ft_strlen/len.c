#include <stdio.h>


int ft_len(char *str)
{   
    int i = 0;

    while (str [i] != '\0')
    {
        i++;
    }
    printf("%d", i);
    return (i);
}

int main()
{
    char str[] = "Hello";
    ft_len(str);
    return (0);
}