#include <unistd.h>

void ft_putchar (char c)
{
    write (1, &c, 1);
}
int main()
{   
    
    char l1 = 'a';
    char l2 = 'B';

    while (l1 <= 'z')
    {
        ft_putchar (l1);
        ft_putchar (l2);
        l1 = l1 + 2;
        l2 = l2 + 2;
    }
    return (0);
}