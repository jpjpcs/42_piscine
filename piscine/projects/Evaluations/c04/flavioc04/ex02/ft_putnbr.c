#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	nr;

	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
	}
	if (nb < 0)
	{
		write (1, "-", 1);
		nb = nb * -1;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	nb = nb % 10;
	nr = nb + '0';
	write (1, &nr, 1);
}

int main (void)
{
	int	nb;

	nb = 7;
	ft_putnbr(nb);
}
